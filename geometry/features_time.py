# geometry/features.py
import numpy as np
from scipy.spatial.distance import pdist
import csv
from time import perf_counter

# ── Hydrophobicity scale (Kyte-Doolittle) ─────────────────────────────────────
# Higher = more hydrophobic. Used to score the local chemical environment.
# Used for identify both surface-exposed regions as well as transmembrane regions. 
# Source: https://resources.qiagenbioinformatics.com/manuals/clcgenomicsworkbench/650/Hydrophobicity_scales.html
HYDROPHOBICITY = {
    "ALA":  1.8, "ARG": -4.5, "ASN": -3.5, "ASP": -3.5, "CYS":  2.5,
    "GLN": -3.5, "GLU": -3.5, "GLY": -0.4, "HIS": -3.2, "ILE":  4.5,
    "LEU":  3.8, "LYS": -3.9, "MET":  1.9, "PHE":  2.8, "PRO": -1.6,
    "SER": -0.8, "THR": -0.7, "TRP": -0.9, "TYR": -1.3, "VAL":  4.2,
}

# ── Formal charge at pH 7 ─────────────────────────────────────────────────────
CHARGE = {
    "ARG": +1, "LYS": +1, "HIS": +0.1,   # positive
    "ASP": -1, "GLU": -1,                  # negative
}

# ── H-bond donors (atoms that donate H) ──────────────────────────────────────
HBOND_DONORS = {"N", "NH1", "NH2", "NZ", "NE", "ND1", "ND2", "NE2",
                 "OG", "OG1", "OH", "NE1"}

# ── H-bond acceptors (atoms that accept H) ───────────────────────────────────
HBOND_ACCEPTORS = {"O", "OD1", "OD2", "OE1", "OE2", "OH",
                    "OG", "OG1", "ND1", "NE2", "SD"}

# ── Canonical amino acids for one-hot encoding (20 aa) ───────────────────────
AMINO_ACIDS = [
    "ALA", "ARG", "ASN", "ASP", "CYS", "GLN", "GLU", "GLY", "HIS", "ILE",
    "LEU", "LYS", "MET", "PHE", "PRO", "SER", "THR", "TRP", "TYR", "VAL",
]

# creates a lookup dictionary mapping each aa to its index (0-19) for one-hot encoding
AA_INDEX = {aa: i for i, aa in enumerate(AMINO_ACIDS)}

HYDROPHOBIC_RESIDUES = {"ALA", "VAL", "ILE", "LEU", "MET", "PHE", "TRP", "PRO", "TYR"}


class FeatureExtractor:
    def __init__(self, protein, neighbor_search, radius=10.0):
        """
        Stores:
        - protein         : Protein object from pdb_parser.py
        - neighbor_search : NeighborSearch object from neighbors.py
        - radius          : local environment radius in Å (default 10 Å)
        """
        self.protein = protein
        self.neighbor_search = neighbor_search
        self.radius = radius
        # Precompute protein centroid once — used for depth feature
        all_coords = np.array([a.coord for a in protein.atoms]) #Creation of a numpy array called `all_coords` by extracting the 3D coordinates `coord` from every atom in the protein object
        self.centroid = all_coords.mean(axis=0) # This computes the centroid (average position) of all atoms and stores it as an instance variable
        self.protein_radius = float(np.max(          
            np.linalg.norm(all_coords - self.centroid, axis=1)
        ))
        self.timers = {
            "neighbor_count": 0.0,
            "mean_dist": 0.0,
            "std_dist": 0.0,
            "min_dist": 0.0,
            "density": 0.0,
            "depth": 0.0,
            "depth_norm": 0.0,
            "surface_curvature": 0.0,
            "pocket_depth": 0.0,
            "hydrophobicity": 0.0,
            "charge": 0.0,
            "h-bond": 0.0,
            "amino acid composition": 0.0,
            "hydrophobic_patch": 0.0,
            "charge_distribution": 0.0,
            "evolutionary": 0.0
        }


    def load_pssm(self, pssm_file):
        """
        Load the PSSM CSV generated by mock_pssm_generator.
        Builds a dict: residue_index (1-based int) → conservation_score (float)
        """
        self.pssm = {}  # { 1: 0.8543, 2: 0.2341, ... }

        with open(pssm_file, "r") as f:
            reader = csv.DictReader(f)  # reads header automatically
            for row in reader:
                idx   = int(row["Residue_Index"])
                score = float(row["Conservation_Score"])
                self.pssm[idx] = score

        print(f"PSSM loaded: {len(self.pssm)} residues")

    def extract(self, sas_point):
        neighbor_idx = self.neighbor_search.query(sas_point, self.radius)
        neighbors    = [self.protein.atoms[i] for i in neighbor_idx]

        geo   = self._geometry_features(sas_point, neighbors)      #  7 features
        phys  = self._physicochemical_features(neighbors)          # 27 features
        curve = np.array([
            self._compute_curvature(sas_point, neighbors),
            self._compute_pocket_depth(sas_point),
            self._hydrophobic_patch_score(sas_point, neighbors),
            self._charge_distribution(sas_point, neighbors)
        ])                                                         #  4 features
        evol  = self._evolutionary_features(sas_point)             #  1 feature

        return np.concatenate([geo, phys, curve, evol])
        # Total: 7 + 27 + 4 + 1 = 39 features


    def extract_all(self, sas_points): # Feature extraction on multiple SAS points
        """
        Extract features for all SAS points at once.
        Returns a 2D matrix of shape (N_points, N_features) thanks to np.array
        """
        # Reset timers for this protein
        for key in self.timers:
            self.timers[key] = 0.0

        # Each call computes one feature vector (geo+phys+curve)
        features = np.array([self.extract(pt) for pt in sas_points])

        # -------- PRINT SUMMARY --------
        print("\n=== FEATURE TIMING (per protein) ===")
        total = sum(self.timers.values())

        for k, v in self.timers.items():
            perc = (v / total * 100) if total > 0 else 0
            print(f"{k:20s}: {v:.2f}s ({perc:.1f}%)")

        print(f"{'TOTAL':20s}: {total:.2f}s\n")

        return features


    # ─────────────────────────────────────────────────────────────────────────
    # Group 1: Geometry
    # ─────────────────────────────────────────────────────────────────────────

    def _geometry_features(self, point, neighbors):
        """
        Captures the shape and spatial density of the local environment.

        Features:
          [0]   neighbor_count     — how many atoms are within radius
          [1]   mean_distance      — average distance to neighbors
          [2]   std_distance       — spread of distances (pocket = low spread)
          [3]   min_distance       — distance to the closest atom
          [4]   density            — atoms per unit volume of the sphere
          [5]   depth              — distance from SAS point to protein centroid
          [6]   depth_norm         — depth normalized by protein radius
        """
        if len(neighbors) == 0:
            # Return zeros if no neighbors found (edge case)
            return np.zeros(7)

        # Distances from the SAS point to each neighboring atom
        coords = np.array([a.coord for a in neighbors])
        dists = np.linalg.norm(coords - point, axis=1)

        t0 = perf_counter()  # Start timer
        # [0] Total neighbor count
        neighbor_count = float(len(neighbors))
        self.timers["neighbor_count"] += perf_counter() - t0

        t0 = perf_counter()  # Start timer
        # [1] Mean distance to neighbors
        mean_dist = float(np.mean(dists))
        self.timers["mean_dist"] += perf_counter() - t0
        
        t0 = perf_counter()  # Start timer
        # [2] Std of distances — low std = tight pocket, high std = flat surface
        std_dist = float(np.std(dists))
        self.timers["std_dist"] += perf_counter() - t0

        t0 = perf_counter()  # Start timer
        # [3] Minimum distance — how close is the nearest atom
        min_dist = float(np.min(dists))
        self.timers["min_dist"] += perf_counter() - t0
        
        t0 = perf_counter()  # Start timer
        # [4] Local density — atoms per Å³ inside the query sphere
        sphere_volume = (4 / 3) * np.pi * (self.radius ** 3)
        density = neighbor_count / sphere_volume
        self.timers["density"] += perf_counter() - t0

        t0 = perf_counter()  # Start timer
        # [5] Depth — distance of the SAS point from the protein's centroid
        #     Points deep inside cavities are close to the centroid
        depth = float(np.linalg.norm(point - self.centroid))    
        self.timers["depth"] += perf_counter() - t0

        t0 = perf_counter()  # Start timer
        # [6] Normalized depth 
        depth_norm = depth / self.protein_radius if self.protein_radius > 0 else 0.0
        self.timers["depth_norm"] += perf_counter() - t0

        return np.array([
            neighbor_count, mean_dist, std_dist,
            min_dist, density, depth, depth_norm
        ])
    
    # --— Advanced geometry: curvature + pocket depth -------------------------

    # SURFACE CURVATURE: Measure how concave or convex a point is. A pocket has a high concavity (negative curvature), aflat surface has ~0, and a protruding tip has a positive curvature
    ## Defines a private method to estimate curvature
    def _compute_curvature(self, point, neighbors, radius=5.0):
        """
        Estimate local curvature using PCA on neighbor coordinates.
        The ratio of the smallest to largest eigenvalue approximates concavity.
        """
        t0 = perf_counter()  # Start timer

        # PCA requires at least 3-4 points of reliable covariance estimation, so defaults to 0.0
        if len(neighbors) < 4:
            return 0.0
        
        coords = np.array([a.coord for a in neighbors]) # Extracts 3D coordinates from neighboring atoms into a NumPy array 
        centered = coords - coords.mean(axis=0) # Centers the coordinates by substracting the mean position 
        cov = np.cov(centered.T) # Computes the covariance matrix of the centered coordinates and transposes for correct orientation
        eigenvalues = np.linalg.eigvalsh(cov)  # sorted ascending
        eigenvalues = np.sort(eigenvalues) # Sorts eigenvalues ascending 
        
        # Low ratio → flat; high ratio → curved/concave
        curvature = eigenvalues[0] / (eigenvalues[-1] + 1e-8) # Divide by epsilon to avoid 0s!!
        
        self.timers["surface_curvature"] += perf_counter() - t0

        return float(curvature)
    
    # POCKET DEPTH
    def _compute_pocket_depth(self, point, n_rays=20):
        """
        Cast rays outward from the point. 
        Fraction of blocked rays ≈ how enclosed the point is.
        """
        t0 = perf_counter()  # Start timer

        directions = self._fibonacci_sphere(n_rays) # _fibonacci_sphere is the helper method
        blocked = 0 # initialize the ocunter for the num,ber of rays that are blocked by protein atoms
        
        for d in directions: # loops over each direction vector from the Fibonacci sphere
            # Sample points along the ray at 0.5Å intervals
            for step in np.arange(0.5, 8.0, 0.5): # intervals from 0.5 to 8.0 (up to 16 steps per ray)
                ray_point = point + d * step
                hits = self.neighbor_search.query(ray_point, radius=1.5) #Queries the KD-tree for atoms within 1.5A of the ray point (simulates a small probe hitting the protein surface)
                if len(hits) > 0: #if any atoms are found, increements the blocked counter and breaks the inner loop
                    blocked += 1
                    break
        
        self.timers["pocket_depth"] += perf_counter() - t0

        return float(blocked) / n_rays  # 0 = exposed, 1 = fully enclosed
        # Return the fraction of blocked rays

    def _fibonacci_sphere(self, n):
        indices = np.arange(n, dtype=float)
        phi = np.arccos(1 - 2 * indices / n)
        theta = np.pi * (1 + 5**0.5) * indices
        return np.column_stack([
            np.sin(phi) * np.cos(theta),
            np.sin(phi) * np.sin(theta),
            np.cos(phi)
        ])

    # ─────────────────────────────────────────────────────────────────────────
    # Group 2: Physicochemical
    # ─────────────────────────────────────────────────────────────────────────

    def _physicochemical_features(self, neighbors):
        """
        Captures the chemical character of the residues surrounding the SAS point.

        Features:
          [0]     mean_hydrophobicity  — average Kyte-Doolittle score
          [1]     std_hydrophobicity   — spread (mixed = potential interface)
          [2]     total_charge         — net formal charge of local residues
          [3]     n_positive           — count of positively charged residues
          [4]     n_negative           — count of negatively charged residues
          [5]     n_hbond_donors       — count of H-bond donor atoms
          [6]     n_hbond_acceptors    — count of H-bond acceptor atoms
          [7-26]  one_hot_aa           — fraction of each of the 20 amino acids
                                         in the local neighborhood
        """
        if len(neighbors) == 0:
            return np.zeros(27)

        # ── Hydrophobicity ────────────────────────────────────────────────────
        t0 = perf_counter()  # Start timer
        hydro_scores = [
            HYDROPHOBICITY.get(a.residue_name, 0.0) for a in neighbors
        ]
        mean_hydro = float(np.mean(hydro_scores))
        std_hydro  = float(np.std(hydro_scores))
        self.timers["hydrophobicity"] += perf_counter() - t0

        # ── Charge ────────────────────────────────────────────────────────────
        # Use unique residues to avoid counting a residue multiple times
        # (a residue has many atoms but one charge)
        t0 = perf_counter()  # Start timer
        seen_residues = set() # creates a set to track unique residues already processed (preventing duplicates)
        charge_values = [] # creates a list to collect the charge values of unique residues
        n_positive = 0
        n_negative = 0

        for atom in neighbors:
            key = (atom.chain_id, atom.residue_id) # creates a unique identifier for the reasidue, using chain id and residue id
            if key not in seen_residues:
                seen_residues.add(key)
                c = CHARGE.get(atom.residue_name, 0) # 0 defaults to 0 if not found
                charge_values.append(c)
                if c > 0:
                    n_positive += 1
                elif c < 0:
                    n_negative += 1

        total_charge = float(sum(charge_values))
        n_positive   = float(n_positive)
        n_negative   = float(n_negative)
        self.timers["charge"] += perf_counter() - t0

        # ── H-bond donors and acceptors ───────────────────────────────────────
        # Count at the atom level — atom name (e.g. "NH1") determines role
        t0 = perf_counter()  # Start timer
        n_donors    = float(sum(1 for a in neighbors if a.name in HBOND_DONORS)) #generator expression to count matches efficiently
        n_acceptors = float(sum(1 for a in neighbors if a.name in HBOND_ACCEPTORS))
        self.timers["h-bond"] += perf_counter() - t0

        # ── Amino acid composition (one-hot fraction) ─────────────────────────
        # Instead of a hard one-hot, we use the *fraction* of each aa type
        # among neighboring residues — more informative for variable-size windows
        t0 = perf_counter()  # Start timer
        aa_counts = np.zeros(20)
        seen_for_aa = set()

        for atom in neighbors:
            key = (atom.chain_id, atom.residue_id)
            if key not in seen_for_aa and atom.residue_name in AA_INDEX:
                seen_for_aa.add(key)
                aa_counts[AA_INDEX[atom.residue_name]] += 1

        total_residues = aa_counts.sum()
        aa_fractions = aa_counts / total_residues if total_residues > 0 else aa_counts

        self.timers["amino acid composition"] += perf_counter() - t0

        return np.concatenate([
            [mean_hydro, std_hydro, total_charge,
             n_positive, n_negative, n_donors, n_acceptors],
            aa_fractions   # 20 values
        ])  # total: 7 + 20 = 27 features
    
    
    # -- CHEMICAL ENVIRONMENT: Hydrophobic patches + charge distribution -------------------------

    # Hydophobic patch score: not just the mean hydrophobicity but whether the hydrophobic atoms are clustered together (a patch) vs scattered

    def _hydrophobic_patch_score(self, point, neighbors):
        """
        Measures spatial clustering of hydrophobic atoms near the point.
        A tight cluster = real hydrophobic patch = likely binding site.
        """
        t0 = perf_counter()  # Start timer

        hydrophobic_coords = np.array([
            a.coord for a in neighbors 
            if a.residue_name in HYDROPHOBIC_RESIDUES
        ])
        
        if len(hydrophobic_coords) < 2:
            return 0.0
        
        # Mean pairwise distance between hydrophobic atoms - lower = more clustered
        mean_pairwise = float(np.mean(pdist(hydrophobic_coords)))
        # Invert so higher score = more clustered

        self.timers["hydrophobic_patch"] += perf_counter() - t0

        return 1.0 / (mean_pairwise + 1e-8)
    
    # Charge distribution: the spatial separation between positive and negative charges

    def _charge_distribution(self, point, neighbors):
        """
        Computes a charge dipole moment vector around the SAS point.
        Returns its magnitude — large value = strong charge separation.
        """
        t0 = perf_counter()  # Start timer

        dipole = np.zeros(3)
        
        for atom in neighbors:
            charge = CHARGE.get(atom.residue_name, 0)
            if charge != 0:
                direction = atom.coord - point
                dipole += charge * direction

        self.timers["charge_distribution"] += perf_counter() - t0

        return float(np.linalg.norm(dipole))
    
    # ─────────────────────────────────────────────────────────────────────────
    # Group 3: Evolutionary
    # ─────────────────────────────────────────────────────────────────────────

    def _evolutionary_features(self, sas_point):
        """
        Returns the PSSM conservation score of the nearest residue.
        
        Score close to 1 → highly conserved (important for function/binding)
        Score close to 0 → variable position (less relevant)
        
        Returns a numpy array of shape (1,) so it can be concatenated.
        """
        t0 = perf_counter()  # Start timer

        # Safety check: if PSSM was not loaded, return 0
        if not hasattr(self, "pssm") or len(self.pssm) == 0:
            return np.zeros(1)

        # Find the residue whose center is closest to this SAS point
        best_score = 0.0
        best_dist  = float("inf")

        for residue in self.protein.residues:
            center = residue.get_center()
            dist   = np.linalg.norm(sas_point - center)
            if dist < best_dist:
                best_dist  = dist
                # residue_id is the sequence number - matches the PSSM index
                best_score = self.pssm.get(residue.residue_id, 0.0)

        self.timers["evolutionary"] += perf_counter() - t0

        return np.array([best_score])